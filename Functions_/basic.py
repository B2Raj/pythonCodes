# def keyword is used to define function in python
def printFunc():
    print("Hii B2.")
def calSum(a,b):
    sum=a+b
    print("Sum is:",sum)
def calSub(a,b):
    sub=a-b
    print("Sub is:",sub)
def calMul(a,b):
    product=a*b
    print("Product is:",product)
def calDiv(a,b):
    divide=a/b
    print("Division is:",divide)
def calRem(a,b):
    remainder=a%b
    print("Remainder is:",remainder)
def calAverage(a,b,c):
    average=(a+b+c)/3
    print("Average is:",average)
