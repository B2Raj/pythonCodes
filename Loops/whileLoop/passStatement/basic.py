# pass:- pass is a null statement that does nothing. It is used as a placeholder for future code.

# example1
for ele in range(10):
    print(ele)
# In example 1, there is no pass statement so loop will execute as usual

# example 2
for ele in range(10):
    pass
print("Helloo B2")
# In example 2 there is pass statement so loop will execute but at the time when it hits pass statement then no execution will be there